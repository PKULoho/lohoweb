剧目简介：
　　原创大型沪剧《敦煌女儿》是一部有筋骨、有情怀，展现共产党人坚定信仰和文化坚守的作品。该剧以“一带一路”中重要的文明交汇地“敦煌”为背景，讲述了北京大学优秀校友、敦煌研究院第三任院长樊锦诗以50年的执著扎根坚守于甘肃大漠，从上海小女孩到北大女生再到敦煌学大学者，穷其一生致力敦煌研究的人生历程，讴歌了一代代敦煌人坚守理想的崇高情怀。
