主要演员介绍
茅善玉 饰  樊锦诗
　　著名沪剧表演艺术家、国家一级演员，上海沪剧院院长，国家级非物质文化遗产项目“沪剧”项目代表性传承人。全国宣传文化系统“四个一批”人才。现任全国政协委员、民盟中央委员、中国戏曲现代戏研究会副会长、中国戏剧家协会理事、上海戏剧家协会副主席、上海戏曲艺术中心艺委会主任。2002年2月至今任上海沪剧院院长。
　　1974年考入上海沪剧团学馆，1979年毕业后留团(今上海沪剧院)。曾先后主演《庵堂相会》《一个明星的遭遇》《姊妹俩》《魂断蓝桥》《血染姐妹花》《牛仔女》《碧海青天夜夜心》《今日梦圆》《红灯记》《生死对话》《露香女》《家》《雷雨》《瑞珏》《董梅卿》《邓世昌》《敦煌女儿》等剧目。她还在《璇子》《姊妹俩》《牛仔女》等沪剧电视连续剧和电影《风雨相思雁》、沪剧电影《雷雨》中担任主演，曾先后为来华访问的美国里根总统、德国总理科尔等国家首脑作专场演出。近年多次赴港澳台访问演出，先后出访美国、南非、澳大利亚等国，并于2006年1月应邀在奥地利维也纳金色大厅举行的“中国新春音乐会”上，登台演唱沪剧选曲《燕燕做媒》，使沪剧第一次登上了世界级艺术殿堂。
　　她师承沪剧宗师丁是娥和石筱英，并转益多师，其唱腔兼有“丁派”的华丽多变和“石派”的委婉甜糯，多年来舞台实践让茅善玉对沪剧音乐唱腔有了更深的理解，并不断探索沪剧声腔的丰富性和现代表达，坚持自己设计每段唱腔，以我为主，在传承中发展，吸收了越剧、评弹、锡剧、京剧等特色，并将现代流行歌曲的节奏和气声唱法、中国民族的歌唱技巧，融会于沪剧的声腔之中；她行腔圆润妩媚，旋律丰富大气而婉转跌宕，以真挚、饱满的情感和深沉的文化内蕴传递出磁实的气场和优美的想象，洋溢着上海都市的清新格调和时代气息；其表演力求从人物出发，挖掘内心世界，借助肢体语言的手段，气脉连贯、自然饱满、生动传神，从而形成了独具风格的表演艺术。近年来她塑造的艺术形象深入人心，许多片段和唱段在社会上大量传唱，深得广大观众的喜爱和追捧。
　　她曾先后荣获第二届《大众电视》“金鹰奖”最佳女主角奖，全国现代戏会演主演一等奖，上海市青年演员会演特等奖，第二届、第二十六届中国戏剧“梅花奖”（二度梅），第一、第七届上海白玉兰戏剧表演艺术主角奖，第四届中国戏剧节优秀主角奖，2011年荣获中国戏曲现代戏突出贡献奖，2012年荣获上海优秀文艺工作者奖。2014年荣获上海市五一巾帼奖标兵、上海市五一劳动奖章、奖状、上海文艺家荣誉奖，2014年10月15日参加习近平总书记主持召开的全国文艺工作座谈会，还荣获2015—2016年度上海市三八红旗手标兵，2017年度全国三八红旗手。茅善玉曾任上海市人大代表。
钱思剑 饰 彭金章
　　国家一级演员，国家级非物质文化遗产保护项目“沪剧”项目上海市代表性传承人
