                <br>

信息管理系师生，以一男一女二人组队形式报名参赛；当然亦可以单独报名，体育部将会为你随（强）机（制）匹（分）配（配）队友，保证你可以进行愉快的比赛！
项目介绍
*项目可能根据报名情况进行调整
1.  投篮之星
参赛者投十个篮（男生在罚球线上或三分线外，女生在三秒区外或三分线外）。男生罚球线投进记1分，三分线外记2分。女生三秒区外投进记1分，三分线外记3分。最后根据队伍总得分排名。
2.  天旋地转
参赛者在后场三秒区内低头转5圈，转完后拿起放在罚球线上的球运到前场，男生罚球线投篮，女生任意位置投篮或上篮，进球停表。最后根据队伍总耗时排名。
3.  游龙戏珠
场中设置障碍物摆成"S"型。男生从中线出发后绕障碍物运球前进，经过最后一个障碍物后上篮，命中后将球递给端线处女生。女生接球后出发，绕障碍物运球至原点（球场中线位置）。如若参赛选手运丢球或碰倒障碍物，则回到相应始发点重新开始；男生若上篮不进，可以补篮，补进为止。最后根据队伍总耗时排名。
4.  争分夺秒
比赛以2V1的形式进行，每回合进攻方两名选手，防守方一名选手。
每次进攻，进攻方发球人从后场三分线内发球，接球者在后场接球。进攻限时15秒，两名进攻队员互相配合，只有一次投篮机会，时间超过、投篮未进、球被截下或犯规则进攻结束。若进球得分，男生三分线内记1分，三分线外记2分，女生得分翻倍。
每队四次进攻机会，防守方规定两人必须各守两次，上场次序自定。防守队员若犯规则罚分，对女生犯规进攻方得2分，对男生犯规进攻方得1分。
比赛采用淘汰赛形式，两两对决，决出冠亚军，其余战绩相同的队伍按净胜分排序，净胜分再相同则按得分多少排序。


